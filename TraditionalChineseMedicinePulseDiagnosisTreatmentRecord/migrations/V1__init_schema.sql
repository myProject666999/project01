-- ========================================
-- 中医脉案诊疗记录系统 - 数据库初始化脚本
-- ========================================

-- 创建数据库
CREATE DATABASE IF NOT EXISTS tcm_pulse_record DEFAULT CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;
USE tcm_pulse_record;

-- ========================================
-- 1. 患者表
-- ========================================
CREATE TABLE patient (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(100) NOT NULL COMMENT '姓名',
    gender VARCHAR(20) NOT NULL COMMENT '性别',
    age INT NOT NULL COMMENT '年龄',
    phone VARCHAR(20) COMMENT '联系电话',
    id_card VARCHAR(20) COMMENT '身份证号',
    address VARCHAR(500) COMMENT '地址',
    remark TEXT COMMENT '备注',
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
    updated_at DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    INDEX idx_name (name),
    INDEX idx_phone (phone)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='患者表';

-- ========================================
-- 2. 诊疗记录表
-- ========================================
CREATE TABLE consultation (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    patient_id BIGINT NOT NULL COMMENT '患者ID',
    complexion TEXT COMMENT '面色',
    tongue_appearance TEXT COMMENT '舌象',
    tongue_image_url VARCHAR(500) COMMENT '舌象图片URL',
    breath TEXT COMMENT '口气',
    cough TEXT COMMENT '咳嗽声',
    voice TEXT COMMENT '声音',
    chief_complaint TEXT NOT NULL COMMENT '主诉',
    associated_symptoms TEXT COMMENT '伴随症状',
    stool TEXT COMMENT '大便情况',
    urine TEXT COMMENT '小便情况',
    pulse_types VARCHAR(200) COMMENT '脉象类型(逗号分隔)',
    visit_date DATETIME NOT NULL COMMENT '就诊日期',
    remark TEXT COMMENT '备注',
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (patient_id) REFERENCES patient(id) ON DELETE CASCADE,
    INDEX idx_patient_id (patient_id),
    INDEX idx_visit_date (visit_date)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='诊疗记录表';

-- ========================================
-- 3. 处方表
-- ========================================
CREATE TABLE prescription (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    patient_id BIGINT NOT NULL COMMENT '患者ID',
    consultation_id BIGINT COMMENT '诊疗记录ID',
    diagnosis TEXT COMMENT '诊断',
    total_dosage INT COMMENT '总剂数',
    prescription_usage TEXT COMMENT '用法',
    doctor_name VARCHAR(100) COMMENT '医生姓名',
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (patient_id) REFERENCES patient(id) ON DELETE CASCADE,
    FOREIGN KEY (consultation_id) REFERENCES consultation(id) ON DELETE SET NULL,
    INDEX idx_patient_id (patient_id),
    INDEX idx_created_at (created_at)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='处方表';

-- ========================================
-- 4. 草药表
-- ========================================
CREATE TABLE herb (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(100) NOT NULL COMMENT '药名',
    category VARCHAR(50) COMMENT '分类',
    nature VARCHAR(20) COMMENT '性',
    flavor VARCHAR(20) COMMENT '味',
    channel_tropism VARCHAR(200) COMMENT '归经(逗号分隔)',
    dosage_range VARCHAR(50) COMMENT '用量范围',
    contraindications TEXT COMMENT '禁忌',
    description TEXT COMMENT '说明',
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
    UNIQUE KEY uk_name (name),
    INDEX idx_category (category)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='草药表';

-- ========================================
-- 5. 草药别名表
-- ========================================
CREATE TABLE herb_alias (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    herb_id BIGINT NOT NULL COMMENT '草药ID',
    alias_name VARCHAR(100) NOT NULL COMMENT '别名',
    FOREIGN KEY (herb_id) REFERENCES herb(id) ON DELETE CASCADE,
    UNIQUE KEY uk_herb_alias (herb_id, alias_name),
    INDEX idx_alias_name (alias_name)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='草药别名表';

-- ========================================
-- 6. 处方明细表
-- ========================================
CREATE TABLE prescription_item (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    prescription_id BIGINT NOT NULL COMMENT '处方ID',
    herb_id BIGINT COMMENT '草药ID',
    herb_name VARCHAR(100) NOT NULL COMMENT '草药名称',
    dosage DECIMAL(10,2) NOT NULL COMMENT '剂量(克)',
    preparation_method VARCHAR(50) COMMENT '煎服法',
    FOREIGN KEY (prescription_id) REFERENCES prescription(id) ON DELETE CASCADE,
    FOREIGN KEY (herb_id) REFERENCES herb(id) ON DELETE SET NULL,
    INDEX idx_prescription_id (prescription_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='处方明细表';

-- ========================================
-- 7. 配伍禁忌规则表 (十八反十九畏)
-- ========================================
CREATE TABLE compatibility_rule (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    herb_a_id BIGINT NOT NULL COMMENT '草药A ID',
    herb_b_id BIGINT NOT NULL COMMENT '草药B ID',
    rule_type VARCHAR(50) NOT NULL COMMENT '规则类型: EIGHTEEN_INCOMPATIBILITIES(十八反), NINETEEN_MUTUAL_RESTRAINTS(十九畏)',
    description TEXT COMMENT '描述',
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (herb_a_id) REFERENCES herb(id) ON DELETE CASCADE,
    FOREIGN KEY (herb_b_id) REFERENCES herb(id) ON DELETE CASCADE,
    UNIQUE KEY uk_herb_pair (herb_a_id, herb_b_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='配伍禁忌规则表';

